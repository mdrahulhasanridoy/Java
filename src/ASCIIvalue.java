public class ASCIIvalue {
    public static void main(String[] args) {
        char ch1 = 'a';
        char ch2 = 'z';
        char ch3 = 'A';
        char ch4 = 'Z';

        int number1 = ch1;
        int number2 = ch2;
        int number3 = ch3;
        int number4 = ch4;

        System.out.println(number1);
        System.out.println(number2);
        System.out.println(number3);
        System.out.println(number4);
    }
}
