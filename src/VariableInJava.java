public class VariableInJava {
    public static void main(String[] arga) {
        int a = 10;
        int b = 5;
        System.out.println(a);
        System.out.println(b);
        String name = "Rahul Hasan";
        System.out.println(name);
        a = b;
        System.out.println(a);
    }
}
