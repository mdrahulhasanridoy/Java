public class AssignmentOperators {
    public static void main(String[] args) {

        int a = 10;
        a += 10;//a = a + 10;

        int b = 10;
        b -= 10; //b = b - 10

        int c = 10;
        c *= 10; //c = c * 10;

        int d = 10;
        d /= 10; //d = d / 10;

        int e = 10;
        e %= 10; //e = e / 10;

        System.out.println(a + "\n" + b + "\n" + c + "\n" + d + "\n" + e);
    }
}